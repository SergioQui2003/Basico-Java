    private void BtnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAccederActionPerformed
        try {
            conexionjava Con = new conexionjava();
            String u =TxtUsuario.getText();
            String p = TxtPassword.getText();
            Con.ConectarBasedeDatos();
            
           String SQL = "SELECT id, nombre FROM Usuarios "
            + "WHERE nombre='" + u + "' AND password='" + p + "'";
            
            Con.resultado = Con.sentencia.executeQuery(SQL);
            
            if (Con.resultado.next()){
                setVisible(false);
                Menu Menu = new Menu();
                //Menu.LblUsuario.setText(""+Con.resultado.getNString("nombre"));
                Menu.setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null, "Usuario o contrase침a incorrectos");
            }
            
        } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_BtnAccederActionPerformed

